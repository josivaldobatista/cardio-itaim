    private void jTablePacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePacientesMouseClicked
        String nome_Paciente = "" + jTablePacientes.getValueAt(jTablePacientes.getSelectedRow(), 1);
        conex.conectar();
        conex.executaSql("select * from pacientes where nome_paciente ='" + nome_Paciente + "'");
        try {
            conex.resultSet.first();
            jTextFieldCodigo.setText(String.valueOf(conex.resultSet.getInt("id_paciente")));
            jTextFieldNome.setText(conex.resultSet.getString("nome_paciente"));
            jComboBoxSexo.setSelectedItem(conex.resultSet.getString("sexo_paciente"));
            jFormattedTextFieldRG.setText(conex.resultSet.getString("rg_paciente"));
            jFormattedTextFieldCPF.setText(conex.resultSet.getString("cpf_paciente"));
            jFormattedTextFieldNasc.setText(conex.resultSet.getString("nasc_paciente"));
            jComboBoxFumante.setSelectedItem(conex.resultSet.getString("fumante_paciente"));
            jComboBoxConvenio.setSelectedItem(conex.resultSet.getString("convenio_paciente"));
            jFormattedTextFieldTelFixo.setText(conex.resultSet.getString("telefone_paciente"));
            jFormattedTextFieldCel.setText(conex.resultSet.getString("celular_paciente"));
            jTextFieldEmail.setText(conex.resultSet.getString("email_paciente"));
            jFormattedTextFieldContato.setText(conex.resultSet.getString("contato_paciente"));
            jTextFieldObs.setText(conex.resultSet.getString("obs_paciente"));
            jTextFieldRua.setText(conex.resultSet.getString("rua_paciente"));
            jTextFieldNumeroCasa.setText(conex.resultSet.getString("numrua_paciente"));
            jFormattedTextFieldCEP.setText(conex.resultSet.getString("cep_paciente"));

            //conexao para buscar o bairro no jComboBox.
            ConexaoBD conexPesquisa = new ConexaoBD();
            conexPesquisa.conectar();
            conexPesquisa.executaSql("select *from bairros where id_bairro =" + conex.resultSet.getInt("id_bairro_paciente"));
            conexPesquisa.resultSet.first();
            jComboBoxBairro.setSelectedItem(conexPesquisa.resultSet.getString("nome_bairro"));
            conexPesquisa.desconecta();
            //fim da busca para o nome do bairro.
            jTextFieldCompl.setText(conex.resultSet.getString("compl_paciente"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERRO AO SELECIONAR OS DADOS!" + ex.getMessage());
        }
        //bloqueando botoes
        jButtonSalvar.setEnabled(false);

        //liberando botoes
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);

        //bloqueando os campos
        jTextFieldNome.setEnabled(false);
        jComboBoxSexo.setEnabled(false);
        jFormattedTextFieldRG.setEnabled(false);
        jFormattedTextFieldCPF.setEnabled(false);
        jFormattedTextFieldNasc.setEnabled(false);
        jComboBoxFumante.setEnabled(false);
        jComboBoxConvenio.setEnabled(false);
        jFormattedTextFieldTelFixo.setEnabled(false);
        jFormattedTextFieldCel.setEnabled(false);
        jTextFieldEmail.setEnabled(false);
        jFormattedTextFieldContato.setEnabled(false);
        jTextFieldObs.setEnabled(false);
        //bloqueando os campos endereco
        jTextFieldRua.setEnabled(false);
        jTextFieldNumeroCasa.setEnabled(false);
        jFormattedTextFieldCEP.setEnabled(false);
        jComboBoxBairro.setEnabled(false);
        jTextFieldCompl.setEnabled(false);


    }//GEN-LAST:event_jTablePacientesMouseClicked

