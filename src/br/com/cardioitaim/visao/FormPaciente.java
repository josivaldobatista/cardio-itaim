    public void preencherTabela(String Sql) {

        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Id", "Nome", "CPF", "Convênio", "Telefone", "Celular", "Nascimento", "Bairro"};

        conex.conectar();
        conex.executaSql(Sql);

        try {
            conex.resultSet.first();
            do {//fazendo alteração na tabela          
                dados.add(new Object[]{conex.resultSet.getInt("id_paciente"), conex.resultSet.getString("nome_paciente"),
                    conex.resultSet.getString("cpf_paciente"), conex.resultSet.getString("convenio_paciente"), conex.resultSet.getString("telefone_paciente"),
                    conex.resultSet.getString("celular_paciente"), conex.resultSet.getString("nasc_paciente"), conex.resultSet.getString("nome_bairro")});
            } while (conex.resultSet.next());//enquanto houver dados ele ira percorrer o registro.
        } catch (SQLException ex) {
//            JOptionPane.showMessageDialog(rootPane, "BUSQUE POR OUTRO MÉDICO PARA PREENCHER A TABELA!");
        }
        ModeloTabela modelotab = new ModeloTabela(dados, colunas);
        jTablePacientes.setModel(modelotab);
        //coluna 1 ID
        jTablePacientes.getColumnModel().getColumn(0).setPreferredWidth(35);
        jTablePacientes.getColumnModel().getColumn(0).setResizable(false); //bloqueando o redimencionamento pelo mouse
        //coluna 2 Nome
        jTablePacientes.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTablePacientes.getColumnModel().getColumn(1).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 3 CPF
        jTablePacientes.getColumnModel().getColumn(2).setPreferredWidth(85);
        jTablePacientes.getColumnModel().getColumn(2).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 4 Convenio
        jTablePacientes.getColumnModel().getColumn(3).setPreferredWidth(130);
        jTablePacientes.getColumnModel().getColumn(3).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 5 Telefone Fixo
        jTablePacientes.getColumnModel().getColumn(4).setPreferredWidth(75);
        jTablePacientes.getColumnModel().getColumn(4).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 6 Celular
        jTablePacientes.getColumnModel().getColumn(5).setPreferredWidth(79);
        jTablePacientes.getColumnModel().getColumn(5).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 7 Data de Nascimento
        jTablePacientes.getColumnModel().getColumn(6).setPreferredWidth(80);
        jTablePacientes.getColumnModel().getColumn(6).setResizable(false);//bloqueando o redimencionamento pelo mouse
        //coluna 8 Bairro
        jTablePacientes.getColumnModel().getColumn(7).setPreferredWidth(102);
        jTablePacientes.getColumnModel().getColumn(7).setResizable(false);//bloqueando o redimencionamento pelo mouse

        //bloqueando a reordenação pelo mouse
        jTablePacientes.getTableHeader().setReorderingAllowed(false);
        //bloqueia o redimencionamento automatico.
        jTablePacientes.setAutoResizeMode(jTablePacientes.AUTO_RESIZE_OFF);
        jTablePacientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }

